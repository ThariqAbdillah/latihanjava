public class Matematika {
    int tambah(int nilai1,int nilai2){
        int hasil = nilai1 + nilai2;
        return hasil;
        
    }
    int Kurang(int nilai1,int nilai2){
        int hasil = nilai1 - nilai2;
        return hasil;
    }
    int kali(int nilai1,int nilai2){
        int hasil = nilai1 * nilai2;
        return hasil;
    }
    int bagi(int nilai1,int nilai2){
        int hasil = nilai1 / nilai2;
        return hasil;
    }
    
}
    
    

    
